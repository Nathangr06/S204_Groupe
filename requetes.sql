CREATE OR REPLACE VIEW Q7(IDP, NBR_BIEN) AS
SELECT IdP, COUNT(IdEBien)
FROM BIEN
WHERE BIEN.Type = 'STUDIO'
GROUP BY IDP
HAVING COUNT(IDEBIEN) >= 2;

SELECT Q7.IDP, COUNT(BIEN.IDEBIEN) FROM Q7, BIEN
WHERE Q7.IDP = BIEN.IDP
GROUP BY Q7.IDP;

